<br>
<span style="font-weight: bold;">Insulin glargine</span><br>
Insulin glargine is a long-acting form of the insulin hormone,
prescribed for diabetes along with proper diet and exercise program.<br>
<br>
<span style="font-weight: bold;">Insulin Glulisine</span><br>
Insulin Glulisine is a fast-acting form of the hormone insulin,
prescribed for diabetes mellitus.<br>
<br>
<span style="font-weight: bold;">Insulin Lispro</span><br>
Insulin Lispro is a fast acting insulin analogue, prescribed for type 1
diabetes.<br>
<br>
<span style="font-weight: bold;">Linagliptin</span><br>
Linagliptin is a dipeptidyl peptidase-4 (DPP-4) inhibitor, prescribed
for type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Metformin</span><br>
Metformin is an oral antidiabetic agent, prescribed for type 2
diabetes. It helps control blood sugar levels.<br>
<br>
<span style="font-weight: bold;">Miglitol</span><br>
Miglitol is an oral anti-diabetic drug (alpha-glucosidase inhibitor),
prescribed for type 2 diabetes either alone or with other medications.<br>
<br>
<span style="font-weight: bold;">Nateglinide</span><br>
Nateglinide is an antidiabetic agent, prescribed for type 2 diabetes.
It lowers blood sugar level by speeding up the release of insulin from
the pancreas. It is used along with diet and exercise.<br>
<br>
<span style="font-weight: bold;">Pioglitazone</span><br>
Pioglitazone is a thiazolidinedione antidiabetic, prescribed for type 2
diabetes in certain patients. It is used along with diet and exercise.
It may be used alone or with other antidiabetic medicines. It lowers
blood sugar by reducing insulin resistance.<br>
<br>
<span style="font-weight: bold;">Pramlintide Acetate
Injection</span><br>
Pramlintide Acetate Injection is an amylin analog, prescribed for
diabetes.<br>
<br>
<span style="font-weight: bold;">Repaglinide</span><br>
Repaglinide is a meglitinide antidiabetic, prescribed for type 2
(non-insulin-dependent) diabetes.<br>
<br>
<span style="font-weight: bold;">Saxagliptin</span><br>
Saxagliptin is a new oral anti-diabetic agent, prescribed for type 2
diabetes with diet and exercise.<br>
<br>
<span style="font-weight: bold;">Sitagliptin</span><br>
Sitagliptin is a dipeptidyl peptidase-4 (DPP-4) inhibitor, prescribed
for type 2 diabetes (condition in which the body does not use insulin
normally and therefore cannot control the amount of sugar in the
blood), particularly in people whose diabetes cannot be controlled by
diet alone.&nbsp;<br>
<br>
<span style="font-weight: bold;">Tolazamide</span><br>
Tolazamide is a sulfonylurea antidiabetic, prescribed for type 2
diabetes either alone or with other medications.<br>
<br>
<span style="font-weight: bold;">Tolbutamide</span><br>
Tolbutamide is an antidiabetic agent, prescribed for type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Vildagliptin</span><br>
Vildagliptin is an oral antidiabetic agent, prescribed for type 2
diabetes mellitus along with other medications.<br>
<br>
<span style="font-weight: bold;">Voglibose</span><br>
Voglibose is an alpha-glucosidase inhibitor, prescribed for diabetes
mellitus.<br>
