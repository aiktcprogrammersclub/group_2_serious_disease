<br>
Glyburide<br>
Glyburide is an antidiabetic compound, prescribed for type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Insulin</span><br style="font-weight: bold;">
Insulin is a hormone produced in the pancreas, prescribed for type 1
diabetes. It is also used for type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Insulin Aspart</span><br>
Insulin Aspart is a fast-acting form of the hormone insulin, prescribed
for diabetes mellitus. It is used with other medium- or long-acting
insulin products injected just below the skin to control high blood
sugar. It helps sugar (glucose) get into the cells.<br>
<br>
<span style="font-weight: bold;">Insulin Detemir</span><br>
Insulin Detemir is a long-acting form of the hormone insulin,
