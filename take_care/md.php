<span style="font-weight: bold;">Atorvastatin</span><br style="font-weight: bold;">
Atorvastatin is an HMG-CoA reductase inhibitor, also known as "statin&#8221;,
prescribed for hyperglycemia.<br>
<br>
<span style="font-weight: bold;">Canagliflozin</span><br style="font-weight: bold;">
Canagliflozin is an antidiabetic (sodium glucose co-transporter 2
(SGLT2) inhibitor), prescribed for type 2 diabetes mellitus along with
diet and exercise.<br>
<br>
<span style="font-weight: bold;">Chlorpropamide</span><br style="font-weight: bold;">
Chlorpropamide is a sulfonylurea antidiabetic drug, prescribed for type
2 diabetes. This medication helps to keep blood sugar levels under
control.<br>
<br>
<span style="font-weight: bold;">Desmopressin</span><br style="font-weight: bold;">
Desmopressin is an antidiuretic synthetic hormone, prescribed for
primary nocturnal enuresis (nighttime bedwetting). It is also used for
coagulation disorders and diabetes.<br>
<br>
<span style="font-weight: bold;">Exenatide</span><br style="font-weight: bold;">
Exenatide is a incretin mimetic, prescribed for type 2 diabetes with
diet and exercise, either alone or with other medications.<br>
<br>
<span style="font-weight: bold;">Glibenclamide and
Metformin</span><br style="font-weight: bold;">
Glibenclamide and Metformin contains sulfonylurea and biguanide
antidiabetic combination, prescribed for type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Gliclazide</span><br style="font-weight: bold;">
Gliclazide is an oral hypoglycemic (anti-diabetic drug), prescribed for
type 2 diabetes.<br>
<br>
<span style="font-weight: bold;">Glimepiride</span><br style="font-weight: bold;">
Glimepiride is a sulfonylurea antidiabetic agent, prescribed for type 2
diabetes.<br>
<br>
<span style="font-weight: bold;">Glipizide</span><br style="font-weight: bold;">
Glipizide is a sulfonylurea antidiabetic agent, prescribed for type 2
diabetes (condition in which the body does not use insulin normally and
therefore cannot control the amount of sugar in the blood),
