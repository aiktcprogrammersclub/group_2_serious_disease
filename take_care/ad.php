<span style="font-weight: bold; font-size:30px; color:green;">What is Medicare?</span><br>
Medicare is a federal health insurance program that pays health care
costs for eligible people who are<br>
age 65 or older<br>
under age 65 with certain disabilities<br>
of any age with end-stage renal disease&#8212;total and permanent kidney
failure that requires a kidney transplant or blood-filtering treatments
called dialysis<br>
What health plans does Medicare offer?<br>
Medicare has four parts:<br>
<ul>
<li>Part A (hospital insurance) covers inpatient care, skilled
nursing home
residence, hospice care, and home health care. Part A has no premium
for those who have paid enough Medicare taxes. A premium is an amount a
person must pay periodically&#8212;monthly or quarterly&#8212;for Medicare, other
health plan, or drug plan coverage. Part A does have a deductible, an
amount a person must pay for health care or prescriptions before the
health plan will pay. A person must pay a daily amount for hospital
stays that last longer than 60 days.</li>
<li>Part B (medical insurance) covers services from health care
providers,
outpatient care, home health care, durable medical equipment, and some
preventative services. Part B has a monthly premium based on a person&#8217;s
income. Rates change each year. After a person pays the deductible each
year, Part B pays 80 percent for most covered services as a primary
payer. The billing staff of the service provider&#8212;hospital or clinic&#8212;can
calculate how much a person will owe.</li>
<li>Part C (Medicare Advantage Plans) are part of Medicare and
are
sometimes called MA Plans. Medicare must approve Medicare Advantage
Plans. Each Medicare Advantage Plan must cover Part A and Part B
services and may cover other services, too. </li>
<li>Medicare Advantage Plans
may have Part D prescription coverage. If not, a person can buy a Part
D plan separately. Medicare Advantage Plans are not all the same. A
person who is thinking of choosing a Medicare Advantage Plan should ask
about the rules of the plan. The rules may specify which health care
providers or hospitals a person may use. The plan may require a
referral from a primary care provider to see a specialist. The plan may
not cover medical expenses incurred during travel. How much a person
has to pay out-of-pocket each year will vary by plan. People who have a
Medicare Advantage Plan cannot have a Medigap plan to help pay
out&#8209;of-pocket costs. See the section on Medigap.</li>
</ul>
