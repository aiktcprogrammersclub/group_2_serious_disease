<span style="font-weight: bold; font-size:30px; color:green;">Does Medicare cover
diabetes services and supplies?</span><br>
Medicare helps pay for the diabetes services, supplies, and equipment
listed below and for some preventive services for people who are at
risk for diabetes. However, coinsurance or deductibles may apply. A
person must have Medicare Part B or Medicare Part D to receive these
covered services and supplies.<br>
Medicare Part B helps pay for diabetes screening tests for people at
risk of developing diabetes ,diabetes self-management training diabetes
supplies such as glucose monitors, test strips, and lancets insulin
pumps and insulin if used with an insulin pump counseling to help
people who are obese lose weight flu and pneumonia shots foot exams and
treatment for people with diabetes eye exams to check for glaucoma and
diabetic retinopathy medical nutrition therapy services for people with
diabetes or kidney
disease, when referred by a health care provider therapeutic shoes or
inserts, in some cases<br>
Medicare Part D helps pay for diabetes medications insulin, excluding
insulin used with an insulin pump diabetes supplies such as needles and
syringes for injecting insulin. People who are in a Medicare Advantage
Plan or other Medicare health
plan should check their plan&#8217;s membership materials and call for
details about how the plan provides the diabetes services, supplies,
and medications covered by Medicare.<br>
