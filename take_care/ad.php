How costly is diabetes management and treatment?</span><br>
Diabetes management and treatment is expensive. According to the
American Diabetes Association (ADA), the average cost of health care
for a person with diabetes is $13,741 a year&#8212;more than twice the cost
of health care for a person without diabetes. Many people who have
diabetes need help paying for their care. For
those who qualify, a variety of government and nongovernment programs
can help cover health care expenses. This publication is meant to help
people with diabetes and their family members find and access such
resources.<br>
<br>
<span style="font-weight: bold; font-size:30px; color:green;">What is health insurance?</span><br>
Health insurance helps pay for medical care, including the cost of
diabetes care. Health insurance options include the following:<br>
private health insurance, which includes group and individual health
insurance<br>
government health insurance, such as Medicare, Medicaid, the Children&#8217;s
Health Insurance Program (CHIP), TRICARE, and veterans&#8217; health care
programs<br>
Starting in 2014, the Affordable Care Act (ACA) prevents insurers from
denying coverage or charging higher premiums to people with preexisting
conditions, such as diabetes. <br>
<br>
<span style="font-weight: bold; font-size:30px; color:green;">What is private health
insurance?</span><br>
Insurance companies sell private health insurance plans. Two types of
private health insurance are Group health insurance. People may be
eligible to purchase group health
insurance through their employer or union or through a family member&#8217;s
employer or union. Other organizations, such as professional or alumni
organizations, may also offer group health insurance.<br>
Individual health insurance. People may purchase individual health
insurance for themselves and their families. The website HealthCare.gov
provides information about individual insurance plans. The website also
provides a search function, called the Health Insurance Marketplace, to
find health insurance options by state. Depending on their income and
family size, some people may qualify for lower-cost premiums through
the Health Insurance Marketplace. People can select or change
individual health insurance plans during the open enrollment period
each year. HealthCare.gov lists open enrollment period dates. The
website also provides information about life events that may allow
people to enroll outside the open enrollment period.<br>
Employers may have a waiting period before an employee and his or her
family members can enroll in the company health plan. Under the ACA,
the waiting period can be no longer than 90 days. Certain health plans
called health maintenance organizations (HMOs) may have an affiliation
period&#8212;a time that must pass before health insurance coverage becomes
effective. An affiliation period can be no longer than 3 months.<br>
