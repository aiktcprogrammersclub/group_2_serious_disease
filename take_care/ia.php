<span style="font-size: 30px; color:green; font-family: 'Times New Roman';">Increasing&nbsp;costs&nbsp;of&nbsp;medicines</span></big>
<br><span style="font-size:18px; ">
The substantial morbidity caused by asthma suggests that the disease is
associated with a large economic burden. The current study analysed the
burden of asthma in eight countries in the Asia-Pacific region. Annual
per-patient direct costs ranged from US$108 for Malaysia to
US$1,010 for Hong Kong. When productivity costs were included, total
per-patient societal costs ranged from US$184 in Vietnam to US$1,189 in
Hong Kong. Urgent care costs were responsible for 18&#8211;90% of total
per-patient direct costs. Overall, total per-patient direct costs were
equivalent to 13% of per capita gross domestic product and 300% of per
capita healthcare spending. Extremes of age, greater severity of
asthma, and poorer general health status were predictive of high cost.<br>
The per-patient cost of asthma in these countries is high, particularly
when seen in the context of overall per-patient healthcare spending.
Strategies to improve asthma control are likely to not only improve
patient outcomes, but also to decrease societal costs.<br>
<br>
<span style="font-weight: bold; font-size:30px; color:green;">Per-patient indirect and
societal costs</span><br>
Overall, mean±SE total per-patient societal costs were US$435±17,
ranging from a low of US$184±16 in Vietnam to a high of US$1,189±105 in
Hong Kong&nbsp;. This represents 16% of per capita GDP when all
countries are pooled. The per capita GDP societal costs of asthma were
particularly high in China (39%), Vietnam (46%), and the Philippines
(28%).<br>
<br>
<span style="font-weight: bold; font-size:30px; color:green;">Determinants of
healthcare expenditure</span><br>
The multivariate analysis identified several factors that are
predictive of increased healthcare expenditures. Extremes of
age (&lt;10&#8197;yrs and &gt;60&#8197;yrs), asthma severity and general
health status were significantly predictive of higher asthma-related
costs. Predicted costs for patients aged 11&#8211;60&#8197;yrs were 35&#8211;60% lower
than those for the very young or the very old. Not unexpectedly, there
was also a significant and graded relationship between the severity of
asthma and the general health status and costs. For example, persistent
asthma predicted higher costs compared with intermittent asthma. Severe
persistent patients had a ratio of 2.51 (range 2.08&#8211;3.02) compared with
those with intermittent asthma.<br>
