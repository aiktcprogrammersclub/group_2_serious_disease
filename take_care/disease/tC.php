<span style="color: rgb(102, 51, 0); font-weight: bold;">Chemotherapy</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Chemotherapy is the
use of drugs to kill cancer cells. However, when most people use the
word chemotherapy they are referring specifically </span><span style="color: rgb(102, 51, 0);">to drug treatments for
cancer that destroy cancer cells by stopping their ability to grow and
divide. Your doctor may call this standard </span><span style="color: rgb(102, 51, 0);">chemotherapy, traditional
chemotherapy, or cytotoxic chemotherapy. These powerful medications
circulate in the bloodstream and directly </span><span style="color: rgb(102, 51, 0);">damage the cells that are
actively growing. Because cancer cells generally grow and divide faster
than normal cells, they are more susceptible </span><span style="color: rgb(102, 51, 0);">to the action of these
drugs.&nbsp; However, damage to healthy cells is unavoidable, and
this damage accounts for the side effects linked to these drugs.</span><span style="color: rgb(102, 51, 0);">Several other types of drugs
are also used to treat cancer, including hormone therapy and various
types of &#8220;targeted&#8221; therapy. In fact, many </span><span style="color: rgb(102, 51, 0);">of the drugs recently
approved for use in cancer treatment fall into these categories.
Because these treatments are much more specific to cancer cells,</span><span style="color: rgb(102, 51, 0);">&nbsp;there is usually
less damage to normal cells, leading to different side effects.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">&nbsp;</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Immunotherapy
</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Immunotherapy (also
called biologic therapy or biotherapy) is a type of cancer treatment
designed to boost the body's natural defences to </span><span style="color: rgb(102, 51, 0);">&nbsp;fight the cancer.
It uses materials either made by the body or in a laboratory to
improve, target, or restore immune system function. </span><span style="color: rgb(102, 51, 0);">&nbsp;Although it is not
entirely clear how immunotherapy treats cancer, it may work by stopping
or slowing the growth of cancer cells, stopping cancer </span><span style="color: rgb(102, 51, 0);">&nbsp;from spreading to
other parts of the body, or helping the immune system increase its
effectiveness at eliminating cancer cells.</span><span style="color: rgb(102, 51, 0);"> There are several types of
immunotherapy, including monoclonal antibodies, non-specific
immunotherapies, and cancer vaccines.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Radiation
as therapy</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Doctors known as
radiation oncologists oversee radiation therapy, which usually consists
of a specific number of treatments given over a set</span><span style="color: rgb(102, 51, 0);">&nbsp;period of time.
The goal of this treatment is to destroy cancer cells without harming
nearby healthy tissue. Radiation therapy may be used </span><span style="color: rgb(102, 51, 0);">&nbsp;as the main
treatment or as an adjuvant therapy (treatment given after the main
treatment to target any potential remaining cancer cells). </span><span style="color: rgb(102, 51, 0);">&nbsp;Radiation therapy
can also be used to shrink tumors and reduce pressure, pain, and other
symptoms of cancer (called palliative radiation therapy)</span><span style="color: rgb(102, 51, 0);">&nbsp;when it is not
possible to completely eliminate the disease.</span><span style="color: rgb(102, 51, 0);">More than half of all people
with cancer receive some type of radiation therapy. For some cancers,
radiation therapy alone is an effective </span><span style="color: rgb(102, 51, 0);">treatment; however, other
types of cancer respond best to combination treatment approaches that
may include radiation plus surgery, chemotherapy, </span><span style="color: rgb(102, 51, 0);">or immunotherapy.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Complementary
and alternative medicine or CAM</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">CAM is the term used
to describe treatments that aren&#8217;t part of conventional cancer
treatment (such as chemotherapy, surgery, and radiation therapy).</span><span style="color: rgb(102, 51, 0);">&nbsp;Complementary
medicine is used in addition to conventional medicine; this may also be
referred to as integrative medicine. Alternative medicine, </span><span style="color: rgb(102, 51, 0);">&nbsp;on the other hand,
is a treatment that is used in place of conventional therapy.</span><span style="color: rgb(102, 51, 0);">People often use
complementary medicine to manage side effects of conventional cancer
treatment or improve their physical or emotional well-being.</span><span style="color: rgb(102, 51, 0);">&nbsp;A few examples of
complementary therapies include acupuncture,<br>
yoga, massage, relaxation techniques, physical fitness activities, art
therapy, </span><span style="color: rgb(102, 51, 0);">&nbsp;music
therapy, nutrition counseling, and dietary and herbal
supplements.However, not all complementary therapies have been
well-studied in </span><span style="color: rgb(102, 51, 0);">&nbsp;people
with cancer. Before beginning a complementary therapy, discuss it with
your doctor or other member of your health care team to find out</span><span style="color: rgb(102, 51, 0);"> if it is safe or if it
interferes with your treatment. For instance, the herb kava can
interfere with anesthesia for surgery and has been found</span><span style="color: rgb(102, 51, 0);">&nbsp;to cause liver
damage. Other herbs and supplements may raise your risk of bleeding
after surgery or make chemotherapy less effective. On the </span><span style="color: rgb(102, 51, 0);">&nbsp;other hand, yoga
can improve the quality of life for people with breast cancer who are
undergoing radiation therapy, and ginger can help reduce </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">nausea from
chemotherapy.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">&nbsp;</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Cancer
surgery</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Surgery is the
removal of the tumor and surrounding tissue during an operation. A
doctor who specializes in treating cancer using surgery </span><span style="color: rgb(102, 51, 0);">is called a surgical
oncologist. Surgery is the oldest type of cancer therapy and remains an
effective treatment for many types of cancer today. </span><span style="color: rgb(102, 51, 0);">The goals of surgery vary.
It is often used to remove all or some of the cancerous tissue after
diagnosis. However, it can also be used to diagnose </span><span style="color: rgb(102, 51, 0);">cancer, find out where the
cancer is located, whether it has spread, and whether it is affecting
the functions of other organs in the body. </span><span style="color: rgb(102, 51, 0);">In addition, surgery can be
helpful to restore the body's appearance or function or to relieve side
effects.</span><span style="color: rgb(102, 51, 0);">
The location where you have surgery depends on the extent of the
surgery and how much recovery is needed. Surgery may be performed in a
doctor's </span><span style="color: rgb(102, 51, 0);">office,
clinic, surgery center, or hospital. Outpatient surgery means that you
do not need to stay overnight in the hospital before or after surgery. </span><span style="color: rgb(102, 51, 0);">Inpatient surgery means that
you do need to stay in the hospital overnight or longer to recover
after the surgery.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);"><br>
<span style="font-weight: bold;">Types of conventional
surgery</span></span><br>
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);"><span style="font-weight: bold;">Diagnostic:</span> For most
types of cancer, a biopsy is the only way to make a definitive
diagnosis. During a surgical biopsy, the surgeon makes a</span><span style="color: rgb(102, 51, 0);">&nbsp;cut in the skin
and removes some or all of the suspicious tissue. There are two main
types of surgical biopsies. An incisional biopsy is the removal </span><span style="color: rgb(102, 51, 0);">&nbsp;of a piece of the
suspicious area for examination. An excisional biopsy is the removal of
the entire suspicious area, such as an unusual mole or a </span><span style="color: rgb(102, 51, 0);">&nbsp;lump.After a
biopsy, the tissue removed is examined under a microscope by a
pathologist. A pathologist is a doctor who specializes in </span><span style="color: rgb(102, 51, 0);">&nbsp;interpreting
laboratory tests and evaluating cells, tissues, and organs to diagnose
disease. The pathologist provides a pathology report to the </span><span style="color: rgb(102, 51, 0);">&nbsp;surgeon or
oncologist, who makes the diagnosis.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);"><span style="font-weight: bold;">Staging:</span> Staging
surgery is performed to find out the size of the tumor and if or where
it has spread. This often includes removing some lymph nodes, </span><span style="color: rgb(102, 51, 0);">which are tiny, bean-shaped
organs that help fight infection, near the cancer to find out if it has
spread there. Together with the physical </span><span style="color: rgb(102, 51, 0);">examination, biopsy, and
results of laboratory and imaging tests, this surgery helps the doctor
decide which kind of treatment is best and predict </span><span style="color: rgb(102, 51, 0);">the patient's prognosis,
which is the chance of recovery.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Tumor removal, also
called curative or primary surgery. The most common type of cancer
surgery is the removal of the tumor and some of the tissue </span><span style="color: rgb(102, 51, 0);">surrounding the tumor. The
tissue surrounding the tumor is called the margin. Tumor removal may be
the only treatment, or it may be combined with </span><span style="color: rgb(102, 51, 0);">chemotherapy, radiation
therapy, or other treatments, which may be given before or after
surgery.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Conventional surgery
requires large cuts, called incisions, through skin, muscle, and
sometimes bone. However, in some situations, surgeons can use</span><span style="color: rgb(102, 51, 0);"> surgical techniques that
are less invasive, which may speed recovery and reduce pain afterwards.
Learn more about types of minimally invasive</span><span style="color: rgb(102, 51, 0);">&nbsp;surgery below.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);"><span style="font-weight: bold;">Debulking:</span> When the
complete removal of a tumor is not possible or might cause excessive
damage to the body, surgery is used to remove as much of the </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">tumor as possible.
Other treatments, such as radiation therapy or chemotherapy, may
sometimes also be used to shrink the remaining cancer.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);"><span style="font-weight: bold;">Palliation:</span>
Palliative surgery is used to relieve side effects caused by a tumor.
It plays an important role in improving quality of life for </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">patients with
advanced cancer or widespread disease.</span><br style="color: rgb(102, 51, 0);">
