<div style="text-align: center; font-weight: bold;"><big>Treatment
for Asthma<br>
</big></div>
<br>
Asthma is not so much "treated" as it is "controlled". As a chronic,
long-term disease, there is no cure. However, there are tools and
medicines to help you control asthma as well as benchmarks to gauge
your progress.<br>
<br>
<span style="font-weight: bold;">The Peak Flow Meter</span><br>
A peak flow meter is a simple, small, hand-held tool that can help you
maintain control of asthma by providing a measurement of how well air
moves out of the lungs.<br>
After blowing into the device, the meter reveals your peak flow number.
A physician will indicate how often to test as well as how to interpret
the result to determine the amount of medication to take. Some people
record scores every morning while others use the peak flow meter
intermittently.<br>
Often, each test with the peak flow meter will be judged against your
"personal best" peak flow number (found during 2 to 3 weeks of good
asthma control). If peak flow tests begin to decline - even before
other symptoms are present - it may indicate a looming asthma attack.
After taking asthma medication, the peak flow meter can be used to test
the effectiveness of drug therapy.<br>
<br>
<span style="font-weight: bold;">Good Control</span><br>
Asthma is considered "well-controlled" if:<br>
<br>
<ul>
<li>Chronic and troublesome symptoms (coughing and shortness of
breath) are
prevented and occur no more than 2 days per week.</li>
<li>There is little need for quick-relief medicines or they are
needed less
than 2 days per week.</li>
<li>You maintain good lung function.</li>
<li>Your activity level remains normal.</li>
<li>Your sleep level remains normal and symptoms do not wake
you from sleep
more than 1 to 2 nights per month.</li>
<li>You do not need emergency medical treatment.</li>
<li>You have no more than one asthma attack each year that
requires
inhalation of corticosteroids.</li>
<li>Your peak flow stays above 80% of your personal best number.</li>
<li>These benchmarks can be obtained by working with a doctor
and avoiding
factors that can make your asthma flare up. Also be sure to treat other
conditions that may interfere with asthma management.</li>
</ul>
Good control also means avoiding things that trigger asthma or asthma
symptoms such as allergens. This may mean limiting time spent outdoors
when pollen levels or air pollution levels are highest and limiting
contact with animals. Asthma linked to allergies can also be suppressed
by getting the necessary allergy shots.<br>
<br>
<span style="font-weight: bold;">Preventive Checkups</span><br>
Part of good asthma control is seeing a doctor every 2 to 6 weeks for
regular checkups until it is under control. Then checkups may be
reduced to once a month or twice a year.<br>
It is a good habit to keep track of asthma symptoms and attacks and
diagnostic numbers such as the peak flow measurement. Doctors and
nurses will ask about these and about daily activities in order to
gauge the status of your asthma control.<br>
<br>
<span style="font-weight: bold;">Medicinemedicine</span><br>
Medication for asthma is broadly categorized as either quick-relief
medicine or long-term control medicine. Reducing airway inflammation
and preventing asthma symptoms is the goal of long-term control
medicines, where as immediate relief of asthma symptoms is the goal of
quick-relief or "rescue" medicines.<br>
Medications can be ingested in pill form, but most are powders or mists
taken orally using a device known as an inhaler. Inhalers permit
medicines to travel efficiently through the airways to the lungs.<br>
<br>
<span style="font-weight: bold;">Inhaler</span><br>
Medication may also be administered using a nebulizer, providing a
larger, continuous dose. Nebulizers vaporize a dose of medication in a
saline solution into a steady stream of foggy vapor that is inhaled by
the patient.<br>
SMART (Single Inhaler Maintenance and Reliever Therapy), is better for
the relief and preventive treatment of asthma symptoms in adults
compared to standard therapy, researchers reported in The Lancet
Respiratory Medicine (March 2013 issue). SMART refers to using ICS
(corticosteroid) plus LABA (long-acting &#946;2 agonist) in one inhaler.<br>
<br>
<span style="font-weight: bold;">Long-Term Control</span><br>
Long-term control medicines are taken every day and are designed to
prevent asthma symptom such as airway inflammation. Inhaled
corticosteroids are the most effective long-term control medicine - the
best at relieving airway inflammation and swelling. They are usually
taken daily to greatly reduce the inflammation that initiates the chain
reaction of the asthma attack.<br>
Even if taken every day, inhaled corticosteroids are not habit-forming.
However, the medicines do have side effects such as the mouth infection
known as "thrush". Thrush occurs when the corticosteroids land in your
throat or mouth. Spacers or holding chambers have been developed to
help avoid this. Thrush can also be avoided by rinsing the mouth out
after inhalation.<br>
Inhaled corticosteroids also increase the risk of cataracts (clouding
of the eye's lens) and osteoporosis (weakening of the bones) if taken
for long periods of time.<br>
There are other long-term control medicines available that doctors may
prescribe. Most of them are taken by mouth and are designed to open the
airways and prevent airway inflammation. Examples include inhaled
long-acting B2-agonists (used with low-dose inhaled corticosteroids),
leukotriene modifiers, cromolyn and nedocromil, and theophylline.<br>
<br>
<span style="font-weight: bold;">Quick-Relief Medicines</span><br>
Quick-relief medicines relieve asthma symptoms when they occur. The
most common of these are inhaled short-acting B2-agonists -
bronchodilators that quickly relax tight muscles around the airways,
allowing air to flow through them.<br>
The quick-relief inhaler should be used when asthma symptoms are first
noticed, but should not be used more than 2 days a week. Most people
carry the quick-relief inhaler with them at all times. Quick-relief
medicines usually do not reduce inflammation and therefore should not
be used as a replacement for long-term control medicines.<br>
<br>
<span style="font-weight: bold;">Children</span><br>
Although quick-relief medicines can relieve wheezing in young children,
long-term control medicines will be used to treat infants and young
children if symptoms are likely to persist after 6 years of age.<br>
Like adults, children are treated with inhaled corticosteroids,
montelukast, or cromolyn. Often, treatments will be tried for 4 to 6
weeks and stopped if the desired outcome is not seen. Inhaled
corticosteroids carry the side-effect of slowed growth, but the effect
is generally small and is only apparent for the first few months of
treatment.<br>
<br>
<span style="font-weight: bold;">Elderly</span><br>
Elderly asthma care may require adjustments to prevent interactions
between medicines. Beta blockers, aspirin, pain relievers, and
anti-inflammatory medicines can prevent asthma medicines from working
correctly and may worsen symptoms. In addition, it may be difficult for
older persons to hold their breath for 10 seconds after inhalation of
medicines, but spacers have been developed to help this issue.<br>
The increased osteoporosis risk brought on by inhaled corticosteroids
may be magnified in older adults with weak bones. It is common to take
calcium and vitamin D pills, among other therapies, to keep bones
healthy.<br>
<br>
<span style="font-weight: bold;">Pregnant Women</span><br>
Proper asthma control is necessary for pregnant women in order to
ensure a good supply of oxygen to the fetus. Babies born of asthmatic
mothers have a higher chance of premature birth and lower birth weight.
For pregnant women, the risks associated with having an asthma attack
outweigh any risks associated with asthma medicines.<br>
<br>
<span style="font-weight: bold;">Vitamin D May Reduce
Asthma Symptoms</span><br>
Researchers from King&#8217;s College London have discovered how vitamin D
can reduce asthma symptoms. Catherine Hawrylowicz and team explained in
the Journal of Allergy and Clinical Immunology (May 2013 issue) that
their findings may offer a new way of treating the debilitating and
usually chronic condition.<br>
Asthma patients are currently prescribed steroid tablets, which may
have harmful side effects. There is a type of asthma, however, that is
resistant to steroid therapy. Patients with this type are susceptible
to severe and often life-threatening asthma attacks.<br>
The scientists found that people with asthma have higher levels of
IL-17A (interleukin-17A). IL-17A is part of the immune system that
protects the body against infection. However, this natural compound
also worsens asthma symptoms. Large amounts of IL-17A can reduce the
clinical effects of steroids.<br>
The team found that asthma patients who were on steroids had the
highest levels of IL-17A. They also found that vitamin D significantly
lowers IL-17A production in cells. Hawrylowicz believes vitamin D could
be a safe and useful add-on treatment.<br>
<br>
<span style="font-weight: bold;">Non-medical Treatments</span><br>
Some people treat asthma using unconventional alternative therapies,
but there is little formal data to support the effectiveness of these
methods. There is research, however, that has found acupuncture, air
ionizers, and dust mite control measures, to have little or no effect
on asthma symptoms or lung function. Evidence is inconclusive to
support or reject osteopathic, chiropractic, physiotherapeutic, and
respiratory therapeutic techniques. Homeopathy may mildly reduce the
intensity of symptoms, but this finding is not robust.<br>
