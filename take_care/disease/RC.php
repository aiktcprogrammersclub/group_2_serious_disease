<span style="color: rgb(102, 51, 0); font-weight: bold;">What
are the health
hazards of exposure to asbestos?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">People may be
exposed to asbestos in their workplace, their communities, or their
homes. If products containing asbestos are disturbed, </span><span style="color: rgb(102, 51, 0);">tiny asbestos fibers are
released into the air. When asbestos fibers are breathed in, they may
get trapped in the lungs and remain there</span><span style="color: rgb(102, 51, 0);">&nbsp;for a long time.
Over time, these fibers can accumulate and cause scarring and
inflammation which can affect breathing and lead to serious </span><span style="color: rgb(102, 51, 0);">&nbsp;health
problems.Asbestos has been classified as a known human carcinogen (a
substance that causes cancer) by the U.S. Department of Health and </span><span style="color: rgb(102, 51, 0);">&nbsp;Human Services,
the EPA, and the International Agency for Research on Cancer (Studies
have shown that exposure to asbestos may increase the risk</span><span style="color: rgb(102, 51, 0);">&nbsp;of lung cancer and
mesothelioma (a relatively rare cancer of the&nbsp;</span><span style="color: rgb(102, 51, 0);">thin&nbsp;membranes&nbsp;that
line the chest and&nbsp;abdomen)</span><span style="color: rgb(102, 51, 0);">
thin&nbsp;membranes&nbsp;that line the chest
and&nbsp;abdomen). Although rare, mesothelioma</span><span style="color: rgb(102, 51, 0);">&nbsp;is the most common
form of cancer associated with asbestos exposure. In addition to lung
cancer and mesothelioma, some studies have suggested </span><span style="color: rgb(102, 51, 0);">&nbsp;an association
between asbestos exposure
and&nbsp;gastrointestinal&nbsp;and&nbsp;colorectalcancers,
as well as an elevated risk for cancers of the&nbsp;throat, </span><span style="color: rgb(102, 51, 0);">&nbsp;kidney,&nbsp;esophagus,
and&nbsp;gallbladder. However, the evidence is inconclusive.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">&nbsp;</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Why
is there concern
that hair dyes may cause cancer?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Many people in the
United States and Europe use hair dyes. It is estimated that more than
one-third of women over age 18 and about 10 percent of </span><span style="color: rgb(102, 51, 0);">men over age 40 use some
type of hair dye . Over 5,000 different chemicals are used in hair dye
products, some of which are reported to be </span><span style="color: rgb(102, 51, 0);">carcinogenic
(cancer-causing) in animals . Because so many people use hair dyes,
scientists have tried to determine whether exposure to the </span><span style="color: rgb(102, 51, 0);">chemicals in hair coloring
products is associated with an increased risk of cancer in people.</span><span style="color: rgb(102, 51, 0);"> Early hair dye formulations
contained chemicals, including aromatic amines that were found to cause
cancer in animals. In the mid- to late 1970s, </span><span style="color: rgb(102, 51, 0);">however, manufacturers
changed the components in dye products to eliminate some of these
chemicals. It is not known whether some of the chemicals </span><span style="color: rgb(102, 51, 0);">still used in hair dyes can
cause cancer. Given the widespread use of hair dye products, even a
small increase in risk may have a considerable </span><span style="color: rgb(102, 51, 0);">public health impact.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Although some
studies have linked the personal use of hair dyes with increased risks
of certain cancers of the blood and bone marrow, </span><span style="color: rgb(102, 51, 0);">such as non-Hodgkin lymphoma
(NHL) and&nbsp;leukemia.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Does
acrylamide
increase the risk of cancer?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Studies in rodent
models have found that acrylamide exposure poses a risk for several
types of cancer. However, the evidence from human studies </span><span style="color: rgb(102, 51, 0);">is still incomplete. The
National Toxicology Program and the International Agency for Research
on Cancer consider acrylamide to be a &#8220;probable </span><span style="color: rgb(102, 51, 0);">human carcinogen,&#8221; based on
studies in laboratory animals given acrylamide in drinking water.
However, toxicology studies have shown differences </span><span style="color: rgb(102, 51, 0);">in acrylamide absorption
rates between humans and rodents.</span><span style="color: rgb(102, 51, 0);"> To avoid such limitations
in accurately determining acrylamide exposure, biomarkers of exposure
were recently used in a Danish cohort study </span><span style="color: rgb(102, 51, 0);">designed to evaluate the
subsequent risk of breast cancer in postmenopausal women. Among women
with higher levels of acrylamide bound </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">to the hemoglobin in
their blood, there was a statistically significant increase in risk of
estrogen receptor-positive breast cancer. </span><span style="color: rgb(102, 51, 0);">This finding suggests an
endocrine hormone-related effect, which would be consistent with the
results of a questionnaire-based cohort </span><span style="color: rgb(102, 51, 0);">study in the Netherlands
that found an excess of endometrial and ovarian cancer&#8212;but not of
postmenopausal breast cancer&#8212;associated with </span><span style="color: rgb(102, 51, 0);">higher levels of acrylamide
exposure. Another cohort study from the Netherlands suggested a
positive association between dietary </span><span style="color: rgb(102, 51, 0);">acrylamide and the risk of
renal cell cancer, but not of prostate or bladder cancer.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">What
evidence is
there that HCAs and PAHs in cooked meats may increase cancer risk?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Studies have shown
that exposure to HCAs and PAHs can cause cancer in animal models. In
many experiments, rodents fed a diet </span><span style="color: rgb(102, 51, 0);">supplemented with HCAs
developed tumors of the breast, colon, liver, skin, lung, prostate, and
other organs. Rodents fed PAHs </span><span style="color: rgb(102, 51, 0);">also developed
cancers, including leukemia and tumors of the gastrointestinal tract
and lungs. However, the doses of HCAs and PAHs</span><span style="color: rgb(102, 51, 0);">used in these
studies were very high&#8212;equivalent to thousands of times the doses that
a person would consume in a normal diet.</span><span style="color: rgb(102, 51, 0);">Nevertheless,
numerous epidemiologic studies have used detailed questionnaires to
examine participants&#8217; meat consumption and meat cooking </span><span style="color: rgb(102, 51, 0);">methods to estimate
HCA and PAH exposures. Researchers found that high consumption of
well-done, fried, or barbecued meats was associated </span><span style="color: rgb(102, 51, 0);">with increased risks
of colorectal , pancreatic, and prostate cancer.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">What
is the cancer
risk of women who were exposed to DES before birth?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">The daughters of
women who used DES while pregnant&#8212;commonly called DES daughters&#8212;have
about 40 times the risk of developing clear cell </span><span style="color: rgb(102, 51, 0);">adenocarcinoma of
the lower genital tract than unexposed women. However, this type of
cancer is still rare; approximately 1 in 1,000 DES daughters</span><span style="color: rgb(102, 51, 0);">&nbsp;develops
it.</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">The first DES
daughters who were diagnosed with clear cell adenocarcinoma were very
young at the time of their diagnoses . Subsequent research </span><span style="color: rgb(102, 51, 0);">has shown that the
risk of developing this disease remains elevated as women age into
their 40s .</span><span style="color: rgb(102, 51, 0);">DES
daughters have
an increased risk of developing abnormal cells in the cervix and the
vagina that are precursors of cancer </span><span style="color: rgb(102, 51, 0);">(dysplasia,&nbsp;cervical
intraepithelial neoplasia, and&nbsp;squamous intraepithelial
lesions). These abnormal cells resemble cancer cells, but</span><span style="color: rgb(102, 51, 0);">&nbsp;they do
not invade nearby healthy tissue and are not cancer. They may develop
into cancer, however, if left untreated. </span><span style="color: rgb(102, 51, 0);">&nbsp;Scientists
estimated that DES-exposed daughters were 2.2 times more likely to have
these abnormal cell changes in the </span><span style="color: rgb(102, 51, 0);">&nbsp;cervix
than unexposed women. Approximately 4 percent of DES daughters
developed these conditions because of their exposure. </span><span style="color: rgb(102, 51, 0);">&nbsp;It has
been recommended that DES daughters have a yearly&nbsp;Pap
test&nbsp;and&nbsp;pelvic exam&nbsp;to check for abnormal
cells .DES daughters may </span><span style="color: rgb(102, 51, 0);">&nbsp;also have
a slightly increased risk of breast cancer after age 40. </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">&nbsp;</span><br style="color: rgb(102, 51, 0); font-weight: bold;">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Do
people infected
with human immunodeficiency virus (HIV) have an increased risk of
cancer?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Yes. People infected
with&nbsp;HIV&nbsp;have a substantially higher risk of some
types of cancer compared with uninfected people of the same age . </span><span style="color: rgb(102, 51, 0);">Three of these
cancers are known as "acquired immunodeficiency syndrome
(AIDS)-defining cancers" or "AIDS-defining
malignancies":&nbsp;Kaposi sarcoma, </span><span style="color: rgb(102, 51, 0);">non-Hodgkin
lymphoma, and&nbsp;cervical cancer.
A&nbsp;diagnosis&nbsp;of any one of these cancers marks the
point at which HIV&nbsp;infection&nbsp;has progressed
to&nbsp;AIDS.</span><span style="color: rgb(102, 51, 0);">People
infected with
HIV are several thousand times more likely than uninfected people to be
diagnosed with Kaposi sarcoma, at least 70 times </span><span style="color: rgb(102, 51, 0);">more likely to be
diagnosed with non-Hodgkin lymphoma, and, among women, at least 5 times
more likely to be diagnosed with cervical cancer.</span><span style="color: rgb(102, 51, 0);">In addition, people
infected with HIV are at higher risk of several other types of cancer.
These other malignancies include&nbsp;anal,&nbsp;liver, </span><span style="color: rgb(102, 51, 0);">and&nbsp;lung
cancer, and&nbsp;Hodgkin lymphoma.</span><br>
<span style="color: rgb(102, 51, 0);"></span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Anyone
Can Get Skin
Cancer</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Is
it true that only
people with light skin get skin cancer?</span><span style="color: rgb(102, 51, 0);"><br>
No. Anyone can get
skin cancer. It's more common among people with a light (fair) skin
tone, but skin cancer can affect anyone. Skin cancer can</span><span style="color: rgb(102, 51, 0);">&nbsp;affect
both men and women.</span><span style="color: rgb(102, 51, 0);"><br>
<span style="font-weight: bold;">How can people with
dark skin get skin cancer?</span></span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Although dark skin
does not burn in the sun as easily as fair skin, everyone is at risk
for skin cancer. Even people who don't burn are at </span><span style="color: rgb(102, 51, 0);">risk for skin
cancer. It doesn't matter whether you consider your skin light, dark,
or somewhere in between. You are at risk for skin cancer. </span><span style="color: rgb(102, 51, 0);">Being in the sun can
damage your skin. Sunlight causes damage through ultraviolet, or UV
rays, (they make up just one part of sunlight). </span><span style="color: rgb(102, 51, 0);">Two parts of UV, UVA
and UVB, can both cause damage to skin. Also, the sun isn't the only
cause of skin cancer. There are other causes. </span><span style="color: rgb(102, 51, 0);">That's why skin
cancer may be found in places on the body never exposed to the sun.</span><br style="color: rgb(102, 51, 0);">
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">Tobacco
Facts</span><br style="color: rgb(102, 51, 0);">
<ul>
<li><span style="color: rgb(102, 51, 0);">Tobacco
use is the
leading cause of preventable illness and death in the United States. It
causes many different cancers as well as chronic</span><span style="color: rgb(102, 51, 0);">&nbsp;lung
diseases, such as emphysema and bronchitis, and heart disease.</span></li>
<li><span style="color: rgb(102, 51, 0);">Cigarette
smoking
causes an estimated 443,000 deaths each year, including approximately
49,000 deaths due to exposure to secondhand smoke.</span></li>
<li><span style="color: rgb(102, 51, 0);">Lung
cancer is the
leading cause of cancer death among both men and women in the United
States, and 90 percent of lung cancer deaths among men</span><span style="color: rgb(102, 51, 0);">and
approximately 80 percent of lung cancer deaths among women are due to
smoking.</span></li>
<li><span style="color: rgb(102, 51, 0);">Smoking
causes many
other types of cancer, including cancers of the throat, mouth, nasal
cavity, esophagus, stomach, pancreas, kidney, bladder, </span><span style="color: rgb(102, 51, 0);">and cervix, and
acute myeloid leukemia.</span></li>
<li><span style="color: rgb(102, 51, 0);">People
who smoke
are up to six times more likely to suffer a heart attack than
nonsmokers, and the risk increases with the number of </span><span style="color: rgb(102, 51, 0);">cigarettes smoked.
Smoking also causes most cases of chronic lung disease.</span></li>
<li><span style="color: rgb(102, 51, 0);">In 2011,
an
estimated 19 percent of U.S. adults were cigarette smokers.</span><span style="color: rgb(102, 51, 0);">Nearly 16 percent of
high school students smoke cigarettes.</span></li>
</ul>
<br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0); font-weight: bold;">How
does alcohol
increase the risk of cancer?</span><br style="color: rgb(102, 51, 0);">
<ul>
<li><span style="color: rgb(102, 51, 0);">Researchers
have
identified multiple ways that&nbsp;alcohol&nbsp;may increase
the risk of&nbsp;cancer, including:</span></li>
<li><span style="color: rgb(102, 51, 0);">metabolizing
(breaking down) ethanol in alcoholic drinks to acetaldehyde, which is
a&nbsp;toxicchemical and a probable human&nbsp;carcinogen; </span><span style="color: rgb(102, 51, 0);">acetaldehyde can
damage both&nbsp;DNA&nbsp;(thegenetic&nbsp;material that
makes up&nbsp;genes) and&nbsp;proteins </span></li>
<li><span style="color: rgb(102, 51, 0);">generating&nbsp;reactive
oxygen species&nbsp;(chemically
reactive molecules that contain oxygen), which can damage DNA,
proteins, and lipids (fats) </span><span style="color: rgb(102, 51, 0);">through a process
called&nbsp;oxidation</span></li>
<li><span style="color: rgb(102, 51, 0);">impairing
the
body&#8217;s ability to break down and absorb a variety
of&nbsp;nutrients&nbsp;that may be associated with cancer risk,
including&nbsp;vitamin A; </span><span style="color: rgb(102, 51, 0);">nutrients in
the&nbsp;vitamin B complex, such asfolate;&nbsp;vitamin
C;&nbsp;vitamin D;&nbsp;vitamin E; and&nbsp;carotenoids</span></li>
<li><span style="color: rgb(102, 51, 0);">increasing
blood
levels of&nbsp;estrogen, a sex hormone linked to the risk of breast
cancer</span></li>
<li><span style="color: rgb(102, 51, 0);">Alcoholic
beverages
may also contain a variety of carcinogenic contaminants that are
introduced during fermentation and production, </span><span style="color: rgb(102, 51, 0);">such
as&nbsp;nitrosamines,&nbsp;asbestos&nbsp;fibers,&nbsp;phenols,
andhydrocarbons.</span></li>
</ul>
<span style="color: rgb(102, 51, 0); font-weight: bold;"><br>
How does the
combination of alcohol and tobacco affect cancer risk?</span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">Epidemiologic
research&nbsp;shows that people who use
both&nbsp;alcohol&nbsp;and&nbsp;tobacco&nbsp;have much
greater risks of developing cancers of the&nbsp;oral cavity, </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">pharynx&nbsp;(throat),&nbsp;larynx,
and&nbsp;esophagus&nbsp;than people who use either alcohol or
tobacco alone. In fact,
for&nbsp;oral&nbsp;and&nbsp;pharyngeal cancers, </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">the risks associated
with using both alcohol and tobacco are multiplicative; that is, they
are greater than would be expected from adding the </span><br style="color: rgb(102, 51, 0);">
<span style="color: rgb(102, 51, 0);">individual risks
associated with alcohol and tobacco together .</span><span style="color: rgb(102, 51, 0);"></span><br style="color: rgb(102, 51, 0);">
