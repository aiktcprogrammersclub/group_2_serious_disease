</div><div style="width: 100%; background-color: rgb(255, 197, 140); vertical-align: top; height: 600px; color: rgb(102, 51, 0);"><div style="text-align: center;"><big style="font-weight: bold;"><big><br>Increasing cost of medicines</big></big><br><br></div>Diabetics are using more insulin and paying about twice as much for the
treatment than a decade ago, even though questions remain about whether
the newer forms of the drug are more effective, Yale University
researchers said. Out-of-pocket costs for each insulin prescription rose to $36 in 2010
from $19 in 2000 as patients switched to newer forms of insulin that
cost two to four times more than older versions, research published in
the Journal of the American Medical Association found. The percentage
of diabetics filling insulin prescriptions rose to 15 percent from 9.7
percent.<br>
The newer treatments, called insulin analogs, offer comparable blood
sugar control to older drugs, though are more convenient and cost more,
said Kasia Lipska, the lead study author. The research, however, found
only a slight benefit at reducing severe nighttime hypoglycemia, or low
blood sugar, a condition the newer drugs are designed to prevent.<br>
&#8220;What we&#8217;re showing is we kind of universally switched over to the more
expensive option without that much convincing data about the benefits
of this transition,&#8221; Lipska, an instructor of medicine at Yale
University School of Medicine in New Haven, Connecticut, said in an
interview. &#8220;Given that we are trying to contain our health-care costs,
when we see these types of shifts we have to know whether this is worth
it.&#8221; More studies are needed to compare the different types of diabetes
medicines, she said.<br><span style="font-weight: bold;">
29 Million Diabetics</span><br>
Diabetes, which results when the body doesn&#8217;t use insulin properly or
doesn&#8217;t make the hormone, is the seventh-leading cause of death in the
U.S. Insulin is a hormone secreted by the pancreas that helps the body
control blood sugar. Type 2, in which the body doesn&#8217;t use insulin
properly, accounts for about 95 percent of diabetics, whose numbers
continue to increase.<br>
More than 29 million people in the U.S. have diabetes, an increase from
26 million estimated in 2010, according to a report released yesterday
by the U.S. Centers for Disease Control and Prevention. Newer treatments offer patients more predictable blood sugar control
and are worth the higher costs, said Laurence Kennedy, an
endocrinologist and chairman of the Department of Endocrinology,
Diabetes and Metabolism at the Cleveland Clinic in Ohio, who wasn&#8217;t
involved in the Yale study released yesterday. He said that previous research has shown a benefit in reducing
hypoglycemia in comparison with older insulins, which is one of the
reasons he prefers to prescribe his patients insulin analogs.<br><span style="font-weight: bold;">
Earlier Treatment</span><br>
Most endocrinologists think Type 2 diabetes patients should be given
insulin treatment sooner rather than later. In the past, patients were
resistant because they didn&#8217;t like the idea of injections or they
thought it meant a bad outcome, he said. Now with smaller needles, ease
of use and better patient education more are willing to take insulin.
&#8220;There is no doubt that the analog insulins, particularly the
longer-lasting insulins, have helped patients with Type 2 diabetes
accept insulin as a very worthwhile treatment,&#8221; he said in a telephone
interview. Still, prescription drug prices have been surging and
insulin products
for diabetes have had some of the bigger price increases, according to
a survey conducted for Bloomberg News by Los-Angeles based DRX, a
provider of comparison software for health plans. The survey, reported
in April, found that since late 2007, U.S. prices have jumped at least
160 percent for various forms of Eli Lilly &amp; Co.&#8217;s Humulin. Insulin
analogs include Sanofi&#8217;s Lantus, Novo Nordisk A/S&#8217;s Novolog and
Lilly&#8217;s Humalog.<br><span style="font-weight: bold;">
Claims Data</span><br>
The Yale researchers reviewed claims data from people in the U.S. with
Type 2 diabetes who were privately insured. They examined trends in
insulin use, out-of-pocket costs and episodes of severe low blood sugar
from 2000 through 2010.<br>
Over the study period, the percentage of people with diabetes filling
an insulin prescription rose to 15 percent in 2010 from 9.7 percent in
2000. The type of insulin diabetics were prescribed shifted from 96
percent for human synthetic insulin in 2000 to 92 percent for insulin
analogs in 2010.<br>
