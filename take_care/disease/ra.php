<div style="text-align: center;"><span style="font-weight: bold;"><br>
WHAT CAUSES ASTHMA?<br>
<br>
</span><span style="font-weight: bold;"></span></div>
<span style="font-weight: bold;">Allergies</span><br>
Almost all asthma sufferers have allergies. In fact, over 25% of people
who have hay fever (allergic rhinitis) also develop asthma. Allergic
reactions triggered by antibodies in the blood often lead to the airway
inflammation that is associated with asthma.<br>
Common sources of indoor allergens include animal proteins (mostly cat
and dog allergens), dust mites, cockroaches, and fungi. It is possible
that the push towards energy-efficient homes has increased exposure to
these causes of asthma.<br>
<br style="font-weight: bold;">
<span style="font-weight: bold;">Tobacco Smoke</span><br>
Tobacco smoke has been linked to a higher risk of asthma as well as a
higher risk of death due to asthma, wheezing, and respiratory
infections. In addition, children of mothers who smoke - and other
people exposed to second-hand smoke - have a higher risk of asthma
prevalence. Adolescent smoking has also been associated with increases
in asthma risk.<br>
<br style="font-weight: bold;">
<span style="font-weight: bold;">Environmental Factors</span><br>
Allergic reactions and asthma symptoms are often the result of indoor
air pollution from mold or noxious fumes from household cleaners and
paints. Other indoor environmental factors associated with asthma
include nitrogen oxide from gas stoves. In fact, people who cook with
gas are more likely to have symptoms such as wheezing, breathlessness,
asthma attacks, and hay fever.<br>
Pollution, sulphur dioxide, nitrogen oxide, ozone, cold temperatures,
and high humidity have all been shown to trigger asthma in some
individuals.<br>
During periods of heavy air pollution, there tend to be increases in
asthma symptoms and hospital admissions. Smoggy conditions release the
destructive ingredient known as ozone, causing coughing, shortness of
breath, and even chest pain. These same conditions emit sulfur dioxide,
which also results in asthma attacks by constricting airways.<br>
Weather changes have also been known to stimulate asthma attacks. Cold
air can lead to airway congestion, bronchoconstriction (airways
constriction), secretions, and decreased mucociliary clearance (another
type of airway inefficiency). In some populations, humidity causes
breathing difficulties as well.<br>
<br>
<span style="font-weight: bold;">Obesity</span><br>
Overweight adults - those with a body mass index (BMI) between 25 and
30 - are 38% more likely to have asthma compared to adults who are not
overweight. Obese adults - those with a BMI of 30 or greater - have
twice the risk of asthma. According to some researchers, the risk may
be greater for nonallergic asthma than allergic asthma.<br>
<br>
<span style="font-weight: bold;">Pregnancy</span><br>
The way you enter the world seems to impact your susceptibility to
asthma. Babies born by Caesarean sections have a 20% increase in asthma
prevalence compared to babies born by vaginal birth. It is possible
that immune system-modifying infections from bacterial exposure during
Cesarean sections are responsible for this difference.<br>
When mothers smoke during pregnancy, their children have lower
pulmonary function. This may pose additional asthma risks. Research has
also shown that premature birth is a risk factor for developing asthma.<br>
<br>
<span style="font-weight: bold;">Stress</span><br>
People who undergo stress have higher asthma rates. Part of this may be
explained by increases in asthma-related behaviors such as smoking that
are encouraged by stress. However, recent research has suggested that
the immune system is modified by stress as well.<br>
<br>
<span style="font-weight: bold;">Genes</span><br>
It is possible that some 100 genes are linked to asthma - 25 of which
have been associated with separate populations as of 2005.<br>
Genes linked to asthma also play roles in managing the immune system
and inflammation. There have not, however, been consistent results from
genetic studies across populations - so further investigations are
required to figure out the complex interactions that cause asthma.<br>
Mom and Dad may be partially to blame for asthma, since three-fifths of
all asthma cases are hereditary. The Centers for Disease Control (USA)
say that having a parent with asthma increases a person's risk by three
to six times.<br>
Genetics may also be interacting with environmental factors. For
example, exposure to the bacterial product endotoxin and having the
genetic trait CD14 (single nucleotide polymorphism (SNP) C-159T) have
remained a well-replicated example of a gene-environment interaction
that is associated with asthma.<br>
<br>
Airway Hyperreactivity<br>
Researchers are not sure why airway hyperreactivity is another risk
factor for asthma, but allergens or cold air may trigger hyperreactive
airways to become inflamed. Some people do not develop asthma from
airway hyperreactivity, but hyperreactivity still appears to increase
the risk of asthma.<br>
<br>
<span style="font-weight: bold;">Atopy</span><br>
Atopy - such as eczema (atopic dermatitis), allergic rhinitis (hay
fever), allergic conjunctivitis (an eye condition) - is a general class
of allergic hypersensitivity that affects different parts of the body
that do not come in contact with allergens. Atopy is a risk factor for
developing asthma.<br>
Some 40% to 50% of children with atopic dermatitis also develop asthma,
and it is probable that children with atopic dermatitis have more
severe and persistent asthma as adults.</div>
