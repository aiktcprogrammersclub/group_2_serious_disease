A diabetes diagnosis can be daunting, since management of the disease
requires careful planning. The creation of a personal diabetes
treatment plan is the best way to comprehensively address a patient's
day-to-day health, nutrition and fitness needs. A diabetes treatment
plan enables patients to achieve great quality of life while living
with diabetes.<br>
<br>
Personalized diabetes treatment plan<br>
Patients seeking medical assistance to create a diabetes treatment plan
will find a caring and compassionate team of experts at the Diabetes
Management Program.<br>
Patients with diabetes work with a multidisciplinary team of primary
care physicians, case managers and certified diabetes educators to
establish a treatment plan for successfully managing the disease. <br>
<br>
When needed, patients also benefit from consultations with
ophthalmologists, endocrinologists, podiatrists and other specialist
doctors.<br>
<br>
Typical diabetes treatment plan to manage long-term health:<br>
<ul>
<li>An individual assessment by diabetes educators of a
patient's nutritional, fitness and mental health needs.</li>
<li>Setting goals for managing optimal blood sugar levels.</li>
<li>Prescriptions for oral medication or an insulin management
program.</li>
<li>Assessing a patient's nutrition and developing nutritional
recommendations, as well as helping patients plan meals to better
manage blood sugar levels.</li>
<li>Use of an insulin pump to regulate insulin levels.</li>
<li>Help selecting and using the tools available for monitoring
blood sugar.</li>
<li>Assessment and management of gestational diabetes.</li>
<li>Regular cardiac evaluation and cardiovascular risk
reduction training for patients with heart disease, high blood
pressure, high cholesterol or obesity in addition to their diabetes.</li>
<li>As part of the treatment plan, certified diabetes educators
may suggest classes on topics such as how to make food choices in
restaurants, how to travel more easily with diabetes, what to do when
you're sick, how to manage the stress of diabetes, active exercise as
integral to any diabetes treatment plan, and how to count carbohydrates
and read food labels</li>
</ul>
